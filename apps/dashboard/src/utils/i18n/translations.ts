// Add a type for the translation keys
export type TranslationKey = keyof typeof translations.en;

// Create a hook for accessing translations
export function useTranslations(locale = "en") {
  // Only allow 'en' or 'ar', default to 'en' for anything else
  const safeLocale = locale === "ar" ? "ar" : "en";
  return {
    t: (key: TranslationKey) =>
      translations[safeLocale][key] || translations.en[key],
  };
}
